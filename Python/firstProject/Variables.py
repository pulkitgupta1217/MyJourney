## Variables

# integers
myInt = 7
print(myInt)

# floats
myfloat = 7.0
print(myfloat)
myfloat = float(7)
print(myfloat)

# strings
myString = 'hello'
print(myString)
myString = "hello"
print(myString)

myString = "don't worry about apostrophies if you are using double quotes"
print(myString)

# working with variables
one = 1
two = 2
three = one + two
print(one, "+", two, "=", three)

hello = "hello"
world = "world"
helloworld = hello + " " + world
print(hello, world, helloworld)

print("cant use operators with numbers and strings")

a, b = 3, 4
print(a, b)
